def translate(key: str, language: str = DEFAULT_LANGUAGE, **kwargs) -> str:
    """Return a translated string and safely apply named formatting values."""
    language = language if language in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE
    text = _TRANSLATIONS.get(language, {}).get(key)
    if text is None:
        text = _TRANSLATIONS["en"].get(key, key)
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, ValueError):
            return text
    return text


def zone_display_name(zone_type: str, language: str = DEFAULT_LANGUAGE) -> str:
    """Return the localized display name for an algorithm zone identifier."""
    key = f"zone_{zone_type}"
    return translate(key, language)
